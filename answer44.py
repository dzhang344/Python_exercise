# 题目044：两个3*3的矩阵，实现其对应位置的数据相加，并返回一个新矩阵：
# X = [[12,7,3],
#     [  4,5,6],
#     [  7,8,9]]
# Y = [[5,8,1],
#     [ 6,7,3],
#     [ 4,5,9]]

X = [[12,7,3],
    [  4,5,6],
    [  7,8,9]]
Y = [[5,8,1],
    [ 6,7,3],
     [4, 5, 9]]

Z = X[:]

for i in range(3):
    for j in range(3):
        Z[i][j] = X[i][j] + Y[i][j]

print(Z)
